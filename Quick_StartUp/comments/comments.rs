fn main(){
    // The current line is commented
    /* 
        Multi-line comment, exactly like C
    */
    print!("This is printed");
    print!(" on the same line!");

    /*
     Rust Escape Sequences
     \n: Newline 
     \t: Tab space.
     \r: Carriage Return
     \" Double Quote
     \\ Backward slash
    */

    println!("\n On there should be a new, empty line!");
    println!("\t Tab Space");
    println!("This text will be overwritten \r Only this text will appear on the screen");
    println!("\"Quotes\"");
    println!("Backslash \\");

}
